# ------------------------------------------------------------------ #
#  构建并返回 Graph 对象
# ------------------------------------------------------------------ #

def build_graph() -> Graph:
    """
    根据上方 BUILDINGS 和 ROADS 数据构建图，返回 Graph 实例。
    这是其他模块（app.py 等）导入数据的统一入口。
    """
    g = Graph()

    for item in BUILDINGS:
        name, x, y, category = item
        g.add_node(name, x=x, y=y, category=category)

    success, fail = 0, 0
    for u, v, dist in ROADS:
        if g.add_edge(u, v, dist):
            success += 1
        else:
            fail += 1
            print(f"[WARNING] 道路添加失败: {u}  {v}（顶点不存在或边重复）")

    return g


